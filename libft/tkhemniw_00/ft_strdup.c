#include"libft.h"

char	*ft_strdup(const char *str)
{
	char			*s;
	unsigned int	i;

	i = 0;
	s = malloc(sizeof(char) * (ft_strlen(str)+1));
	if (!s)
		return (NULL);
	while (str[i])
	{
		s[i] = str[i];
		i++;
	}
	s[i] = '\0';
	return (s);
}
